-- CHURN ANALYSIS BASED ON DIFFERENT SEGMENTS

-- COUNT OF CHURNED CUSTOMERS
SELECT
COUNT(*) AS CHURNED_CUSTOMER
FROM churn
WHERE customer_status = 'churned';

-- COUNT OF CHURNED CUSTOMERS BASED ON REASON
SELECT
COUNT(*) AS CHURNED_CUSTOMER,
churn_reason
FROM churn
WHERE customer_status = 'churned'
GROUP BY churn_reason 
ORDER BY CHURNED_CUSTOMER DESC;

-- COUNT OF CHURNED CUSTOMER BASED ON CHURN CATEGORY
SELECT
COUNT(*) AS CHURNED_CUSTOMER,
churn_category
FROM churn
WHERE customer_status = 'churned'
GROUP BY churn_category 
ORDER BY CHURNED_CUSTOMER DESC;

-- COUNT OF CHURNED CUSTOMER BY CONTRACT BASE
SELECT
COUNT(*) AS CHURNED_CUSTOMER,
contract
FROM churn
WHERE customer_status = 'churned'
GROUP BY contract 
ORDER BY CHURNED_CUSTOMER DESC;

