-- TOP 5 REASONS FOR CHURN

WITH REASON AS
(
SELECT 
churn_reason,
COUNT(customer_id) AS TOTAL_CUSTOMER
FROM churn
WHERE customer_status ='churned'
GROUP BY churn_reason
ORDER BY TOTAL_CUSTOMER DESC)

SELECT
churn_reason,
TOTAL_CUSTOMER,
RANK() OVER (ORDER BY TOTAL_CUSTOMER DESC )  AS RANKX
FROM REASON
LIMIT 5;




 

 
 