-- TOTAL CUSTOMER COUNT
SELECT
DISTINCT COUNT(customer_id) AS Customer_count
FROM churn;

-- CUSTOMER COUNT BY GENDER
SELECT
COUNT(*) AS Customers,
Gender
FROM churn 
GROUP BY Gender;

-- CUSTOMERS BY AGE GROUP
SELECT
COUNT(*) AS Customers,
age_group
FROM churn 
GROUP BY age_group
ORDER BY age_group;

-- CUSTOMER COUNT BY MARRIED STATUS
SELECT
COUNT(*) AS Customers,
married
FROM churn 
GROUP BY married;

-- COUNT OF CUSTOMERS BY STATUS
SELECT
COUNT(*) AS Customers,
customer_status
FROM churn 
GROUP BY customer_status
ORDER BY customer_status;

-- COUNT OF CUSTOMERS BY CONTRACT BASE
SELECT
COUNT(*) AS Customers,
contract
FROM churn 
GROUP BY contract
ORDER BY contract;

